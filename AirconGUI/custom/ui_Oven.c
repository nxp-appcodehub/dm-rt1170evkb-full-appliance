uint32_t Oven_Mode = 0;
/*******************************************************************************
 * Function
 ******************************************************************************/
void ui_oven_change_mode(OVEN_MODE_T mode, OVEN_MODE_Dir_T dir)
{

	if(mode != kOVEN_ModeNull)
	{
		Oven_Mode = mode;
	}
	else
	{
		if(dir == kOVEN_ModeDirLeft)
		{
			if(Oven_Mode < 9)
			{
				Oven_Mode++;
			}
		}
		else if(dir == kOVEN_ModeDirRight)
		{
			if(Oven_Mode > 0)
			{
				Oven_Mode--;
			}
		}
	}

}

